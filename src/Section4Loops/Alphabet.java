package Section4Loops;

public class Alphabet {
    public static void main(String[] args) {
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            System.out.print(ch);
        }
        System.out.println();
        for (char ch1 = 'z'; ch1 >= 'a'; ch1--) {
            System.out.print(ch1);
        }


    }
}

